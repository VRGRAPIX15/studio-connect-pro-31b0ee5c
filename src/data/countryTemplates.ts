// Group templates by country
export function getTemplatesByCountry(): Record<string, CountryTemplate[]> {
  return COUNTRY_TEMPLATES.reduce((acc, template) => {
    if (!acc[template.country]) {
      acc[template.country] = [];
    }
    acc[template.country].push(template);
    return acc;
  }, {} as Record<string, CountryTemplate[]>);
}

// Get popular templates
export function getPopularTemplates(): CountryTemplate[] {
  return COUNTRY_TEMPLATES.filter(t => t.popular);
}

// Search templates
export function searchTemplates(query: string): CountryTemplate[] {
  const q = query.toLowerCase();
  return COUNTRY_TEMPLATES.filter(t => 
    t.country.toLowerCase().includes(q) ||
    t.name.toLowerCase().includes(q) ||
    t.documentType.toLowerCase().includes(q)
  );
}

